    private void button_connectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_connectActionPerformed
        if (tf_nick.getText() != null && tf_hostaddress.getText() != null && tf_port.getText() != null) {
            Controller.myNick = tf_nick.getText();
            String host = tf_hostaddress.getText().toString();
            int port = Integer.parseInt(tf_port.getText().toString());
            Controller c = new Controller();
            c.connect(host, port);
            MainGUI.setMyNick(tf_nick.getText());
            dispose();
        }
    }//GEN-LAST:event_button_connectActionPerformed

    private void button_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_addActionPerformed
        if (tf_nick.getText() != null && tf_hostaddress.getText() != null && tf_port.getText() != null) {
            try {
                File file = new File("favouriten.txt");
                PrintWriter writer = new PrintWriter(file);
                writer.println(tf_nick.getText());
                writer.println(tf_hostaddress.getText());
                writer.println(tf_port.getText());
                writer.flush();
                JOptionPane.showMessageDialog(null, "Server hinzugef√ºgt");
            } catch (Exception ex) {
                System.out.println("Fehler bei 'add': \n" + ex.getMessage());
            }
        }
    }//GEN-LAST:event_button_addActionPerformed
